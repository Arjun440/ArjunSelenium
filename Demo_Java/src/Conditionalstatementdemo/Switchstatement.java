package Conditionalstatementdemo;

	public class Switchstatement {

	public static void main(String[] args) {

		int x=4756;
		
		switch (x) {
		case 1:
			
			System.out.println("Poor Performance in team");
			break;
			
		case 2:
			
			System.out.println("Average Performer in team");
			break;
			
		case 3:
			
			System.out.println("Medium Performer in team");
			break;
	
	case 4:
			
			System.out.println("Great Performer in team");
			break;
			
	case 5:
		
		System.out.println("Top Performer in team");
		break;

		default:
			System.out.println("You are fired");
		}
		
	}

}
